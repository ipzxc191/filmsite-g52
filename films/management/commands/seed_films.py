class Command(BaseCommand):
    help = 'Заполняет базу данных набором реальных фильмов, режиссёров, актёров и жанров'

    def add_arguments(self, parser):
        parser.add_argument(
            '--flush',
            action='store_true',
            help='Перед заполнением удалить все существующие фильмы, режиссёров, актёров и жанры',
        )

    def handle(self, *args, **options):
        if options['flush']:
            self.stdout.write('Удаляю старые данные...')
            Film.objects.all().delete()
            Director.objects.all().delete()
            Actor.objects.all().delete()
            Genre.objects.all().delete()

        with transaction.atomic():
            genres = self._create_genres()
            directors = self._create_directors()
            actors = self._create_actors()
            self._create_films(genres, directors, actors)

        self.stdout.write(self.style.SUCCESS(
            f'Готово: {Film.objects.count()} фильмов, '
            f'{Director.objects.count()} режиссёров, '
            f'{Actor.objects.count()} актёров, '
            f'{Genre.objects.count()} жанров.'
        ))

    def _create_genres(self):
        genres = {}
        for name in GENRES:
            # python-slugify сам транслитерирует кириллицу: 'Криминал' -> 'kriminal'
            genre, _ = Genre.objects.get_or_create(
                name=name, defaults={'slug': slugify(name)}
            )
            genres[name] = genre
        self.stdout.write(f'Жанры: создано/найдено {len(genres)}')
        return genres

    def _create_directors(self):
        directors = {}
        for name, bio in DIRECTORS.items():
            director, _ = Director.objects.get_or_create(
                name=name, defaults={'bio': bio}
            )
            directors[name] = director
        self.stdout.write(f'Режиссёры: создано/найдено {len(directors)}')
        return directors

    def _create_actors(self):
        actors = {}
        for name in ACTORS:
            actor, _ = Actor.objects.get_or_create(name=name)
            actors[name] = actor
        self.stdout.write(f'Актёры: создано/найдено {len(actors)}')
        return actors

    def _create_films(self, genres, directors, actors):
        created_count = 0
        for title, year, director_name, genre_names, actor_names, rating, description in FILMS:
            # slug ничего не передаём — Film.save() сам сгенерирует его
            # через slugify(title) и обеспечит уникальность (title-1, title-2, ...)
            film, created = Film.objects.get_or_create(
                title=title,
                year=year,
                defaults={
                    'description': description,
                    'rating': rating,
                    'director': directors[director_name],
                },
            )
            if not created:
                continue

            film.genres.set([genres[g] for g in genre_names])
            film.actors.set([actors[a] for a in actor_names])

            FilmStats.objects.get_or_create(
                film=film,
                defaults={
                    'views_count': random.randint(500, 50_000),
                    'likes_count': random.randint(50, 5_000),
                },
            )
            created_count += 1

        self.stdout.write(f'Фильмы: создано {created_count} новых из {len(FILMS)}')